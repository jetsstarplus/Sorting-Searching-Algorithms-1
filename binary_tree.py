class Node():
    def __init__(self) -> None:
        self.data = None
        self.left = None
        self.right = None

    def setData(self, data):
        self.data = data

    def getData(self):
        return self.data

    def setLeft(self, data):
        self.left = data

    def getLeft(self):
        return self.left

    def setRight(self, data):
        self.right = data

    def getRight(self):
        return self.right
